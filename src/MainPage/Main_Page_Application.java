    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        jTextField2.setEditable(false);
        jTextField3.setEditable(false);
        jTextField5.setEditable(false);

        String searchStudentID = jTextField1.getText().trim();

        if (searchStudentID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a Student ID for search.", "Search Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Call the readStudentData method to get the list of students
        ArrayList<Student> students = Stu_FileHandling.readStudentData("Student Profile.txt");

        // Flag to check if a matching student is found
        boolean found = false;

        // Search for the student with the given ID
        for (Student student : students) {
            if (student.getStudentID().equals(searchStudentID)) {
                jTextField2.setText(student.getStudentID());
                jTextField3.setText(student.getStudentName());
                jTextField5.setText(String.valueOf(student.getCreditBalance()));
                found = true; // Set found to true if a matching student is found
                break;
            }
        }
        
        
        

        

        // Display an error message if no matching student is found
        if (!found) {
            JOptionPane.showMessageDialog(this, "No student found with the entered Student ID.", "Search Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

