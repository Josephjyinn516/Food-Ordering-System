    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        String reloadAmountText = jTextField4.getText().trim();
        
        if(reloadAmountText.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter a reload amount.", "Reload Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
            double reloadAmount = Double.parseDouble(reloadAmountText.substring(3)); // Extract the numeric part (e.g., "RM 100" -> 100)

            String searchStudentID = jTextField1.getText().trim();

            if (searchStudentID.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter a Student ID for the reload.", "Reload Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Reload the amount into the text file
            TopUpBalance.reloadCredit(searchStudentID, reloadAmount);

            Student student = getStudentById(searchStudentID);
            if (student != null) {
                String notificationTitle = "Credit Reloaded";
                String notificationDetails = "Your credit balance has been reloaded. RM " + reloadAmount;
                // Create a new notification
                Notification notification = new Notification(LocalDate.now(), LocalTime.now(), notificationTitle, notificationDetails);

                // Send the notification to the student
                student.receiveNotification(notification);
                
                // Optionally, you can update the displayed credit balance in jTextField5
                jTextField5.setText(String.format("RM %.2f", TopUpBalance.getUpdatedCreditBalance(searchStudentID)));
                    
                
                int dialogResult = JOptionPane.showConfirmDialog(this, "Reload successful! Do you want to print a receipt?", "Print receipt confirmation", JOptionPane.YES_NO_OPTION);
        
                // Check the user's choice
                if (dialogResult == JOptionPane.YES_OPTION) {
                    try {
                        jTextArea1.print();
                    }
                    catch (Exception e){
                    
                    }
                } else {
                    // User clicked "No" or closed the dialog, do nothing
                }
               
            } else {
                JOptionPane.showMessageDialog(this, "Student not found.", "Error", JOptionPane.ERROR_MESSAGE);
            }
                
            } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Please enter a valid reload amount.", "Invalid Amount", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton11ActionPerformed

    
