    private void displayNotifications(ArrayList<Notification> notifications) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows

        for (Notification notification : notifications) {
            Object[] rowData = {
                notification.getDate(),
                notification.getTime(),
                notification.getTitle(),
                notification.getDetails()
            };
            model.addRow(rowData);
        }
    }

    private void displayStuProfile() {
        System.out.println("Number of students in displayStuProfile: " + Students.size());

        if (Students != null && !Students.isEmpty()) {
            student = Students.get(0);
            // Set the values in the text fields
            jTextField1.setText(student.getStudentID());
            jTextField2.setText(student.getStudentName());
            jTextField3.setText(student.getSchoolEmail());
            jTextField4.setText(String.valueOf(student.getCreditBalance()));
            jTextField5.setText(student.getPassword());
        } else {
            // Handle the case where no student data is available
            System.out.println("No student data found.");
        }
    }


