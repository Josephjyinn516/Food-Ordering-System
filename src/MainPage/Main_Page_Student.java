    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String studentID = jTextField1.getText(); // Get the student ID from jTextField1
        Student selectedStudent = findStudentById(studentID); // Find the student with the given ID

        if (selectedStudent != null) {
            System.out.println("Student found: " + selectedStudent.toString()); // Debug statement

            // If the student is found, display their notifications
            ArrayList<Notification> notifications = Notification_FileHandling.readNotificationFile(studentID);
            System.out.println("Number of notifications: " + notifications.size()); // Debug statement
            displayNotifications(notifications);
        } else {
            // Handle the case where the student is not found (optional)
            System.out.println("Student not found");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
  
    private Student findStudentById(String studentID) {
        for (Student s : Students) {
            if (s.getStudentID().equals(studentID)) {
                return s;
            }
        }
        return null; // Return null if the student is not found
    }
