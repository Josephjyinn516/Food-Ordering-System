    private void jPanel20MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel20MouseClicked
        // TODO add your handling code here:
        String itemIdStr = A_ItemId_textField.getText();
        String name = A_Name_textField.getText();
        String priceStr = A_Price_textField.getText();
        String description = A_Desctiption_textField.getText();
        
        if (itemIdStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Id is required.", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return; // Exit the method
        } 
        
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Name is required.", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return; // Exit the method
        } 
        
        if (priceStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Price is required.", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return; // Exit the method
        } 
        
        if (description.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Description is required.", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return; // Exit the method
        }
        
        int itemId = Integer.parseInt(itemIdStr);
        double price = Double.parseDouble(priceStr);
        
        Appetizer newAppertizer = new Appetizer(itemId, name, price, description);
        
        //call function in VItemFileHandle class and store data into file
        boolean appetizerDataSaved = VAppetizerFileHandle.createAppetizerData("C:\\Users\\tanji\\Downloads\\Joseph Java (Admin)\\Java Assignment\\src\\VendorFile\\Appetizer.txt", newAppertizer);
        
        if(appetizerDataSaved = true)
        {
            JOptionPane.showMessageDialog(this, "Item added successfully");
            A_ItemId_textField.setText(null);
            A_Name_textField.setText(null);
            A_Price_textField.setText(null);
            A_Desctiption_textField.setText(null);
        }else{
            JOptionPane.showMessageDialog(this, "Item added failed");
        }
    }//GEN-LAST:event_jPanel20MouseClicked

